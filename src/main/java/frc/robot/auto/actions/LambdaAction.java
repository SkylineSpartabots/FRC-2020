package frc.robot.auto.actions;

/**
 * Add your docs here.
 */
public class LambdaAction implements Action {

    public interface VoidSupplier {
        void f();
    }

    VoidSupplier mF;

    public LambdaAction(VoidSupplier f) {
        this.mF = f;
    }

    @Override
    public void start() {
        mF.f();
    }

    @Override
    public void update() {}

    @Override
    public boolean isFinished() {
        return true;
    }

    @Override
    public void done() {}
}
